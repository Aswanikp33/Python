def sum(a,b):
        "adding two numbers a and b"
        sum=a+b
        return sum
def sub(a,b):
        "substracting two numbers a and b"
        sub=a-b
        return sub
def mul(a,b):
        "multiply two numbers a and b"
        mul=a*b
        return mul
def div(a,b):
        "dividing two numbers a and b"
        div=a/b
        return div

    
    


